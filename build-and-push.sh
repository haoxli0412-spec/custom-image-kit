#!/bin/bash
# ============================================================
# 自定义沙盒镜像 - 构建 & 推送脚本
# ============================================================
set -e

# ----------------------------------------------------------
# 配置区 (请修改为你的实际值)
# ----------------------------------------------------------
REGISTRY="registry.cn-sh-01.sensecore.cn"   # 你的镜像仓库地址
NAMESPACE="ccr-sandbox"                   # 你的命名空间
IMAGE_NAME="sandbox-server-test-autoimage"                  # 镜像名称
TAG="${1:-latest}"                            # 镜像 tag, 默认 latest, 可通过参数传入

FULL_IMAGE="${REGISTRY}/${NAMESPACE}/${IMAGE_NAME}:${TAG}"

# ----------------------------------------------------------
# 前置检查
# ----------------------------------------------------------
echo "============================================================"
echo "  自定义沙盒镜像构建工具"
echo "============================================================"
echo ""
echo "目标镜像: ${FULL_IMAGE}"
echo ""

# 检查 envd 二进制是否存在
if [ ! -f "envd" ]; then
    echo "[错误] 当前目录缺少 envd 二进制文件"
    echo "       请确认 envd 文件在 Dockerfile 同级目录下"
    exit 1
fi

# 检查 Dockerfile
if [ ! -f "Dockerfile" ]; then
    echo "[错误] 当前目录缺少 Dockerfile"
    exit 1
fi

# ----------------------------------------------------------
# 构建
# ----------------------------------------------------------
echo "[1/3] 构建镜像..."
docker build --provenance=false --sbom=false -t "${FULL_IMAGE}" .

echo ""
echo "[2/3] 构建成功: ${FULL_IMAGE}"

# ----------------------------------------------------------
# 推送
# ----------------------------------------------------------
echo ""
read -p "是否推送到镜像仓库? [y/N] " -n 1 -r
echo ""

if [[ $REPLY =~ ^[Yy]$ ]]; then
    echo "[3/3] 推送镜像到 ${REGISTRY}..."
    docker push "${FULL_IMAGE}"
    echo ""
    echo "============================================================"
    echo "  推送完成!"
    echo "  镜像: ${FULL_IMAGE}"
    echo ""
    echo "  下一步: 运行 register-image.sh 将镜像注册到沙盒服务"
    echo "============================================================"
else
    echo ""
    echo "跳过推送。稍后可手动推送:"
    echo "  docker push ${FULL_IMAGE}"
fi
